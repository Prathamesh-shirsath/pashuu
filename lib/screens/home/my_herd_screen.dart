/// ЁЯФ╜ Filter type for herd list
enum HerdFilter { all, cattle, buffalo }

class MyHerdScreen extends StatefulWidget {
  const MyHerdScreen({super.key});

  @override
  State<MyHerdScreen> createState() => _MyHerdScreenState();
}

class _MyHerdScreenState extends State<MyHerdScreen> {
  final User? _currentUser = FirebaseAuth.instance.currentUser;
  final double _borderRadius = 12.0;

  HerdFilter _selectedFilter = HerdFilter.all;

  Future<void> _showEditNameDialog(DocumentSnapshot animalDoc) async {
    final data = animalDoc.data() as Map<String, dynamic>;
    final TextEditingController nameController =
    TextEditingController(text: data['name'] ?? '');

    return showDialog<void>(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          title: const Text('Edit Animal Name'),
          content: TextField(
            controller: nameController,
            decoration: const InputDecoration(hintText: "Enter new name"),
            autofocus: true,
          ),
          actions: <Widget>[
            TextButton(
              child: const Text('Cancel'),
              onPressed: () => Navigator.of(context).pop(),
            ),
            TextButton(
              child: const Text('Save'),
              onPressed: () async {
                final text = nameController.text.trim();
                if (text.isNotEmpty) {
                  await animalDoc.reference.update({'name': text});
                }
                if (context.mounted) Navigator.of(context).pop();
              },
            ),
          ],
        );
      },
    );
  }

  Future<void> _deleteAnimal(DocumentSnapshot animalDoc) async {
    try {
      await animalDoc.reference.delete();
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          backgroundColor: Colors.red,
          content: Text('Animal removed.'),
        ),
      );
    } catch (e) {
      print('Error deleting animal: $e');
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Failed to remove animal.')),
      );
    }
  }

  /// ЁЯФН Decide if a breed is buffalo based on breed label
  bool _isBuffaloBreed(String breedRaw) {
    final b = breedRaw.trim().toLowerCase();
    const buffaloBreeds = [
      'murrah',
      'mehsana',
      'surti',
      'bhadawari',
      'jaffrabadi',
      'nagpuri',
      'nili_ravi',
      // 'banni', // agar model me buffalo treat karte ho to isko uncomment karo
    ];
    return buffaloBreeds.contains(b);
  }

  @override
  Widget build(BuildContext context) {
    final Color primaryColor = Theme.of(context).primaryColor;
    final Color onSurfaceColor = Theme.of(context).colorScheme.onSurface;

    return Scaffold(
      appBar: AppBar(
        title: const Text('My Herd'),
        backgroundColor: primaryColor,
        foregroundColor: Colors.white,
        elevation: 4,
      ),
      body: _currentUser == null
          ? const Center(
        child: Text('Please log in to see your herd.'),
      )
          : StreamBuilder<QuerySnapshot>(
        stream: FirebaseFirestore.instance
            .collection('users')
            .doc(_currentUser!.uid)
            .collection('herd')
            .orderBy('timestamp', descending: true)
            .snapshots(),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            return const Center(child: CircularProgressIndicator());
          }
          if (snapshot.hasError) {
            return Center(
              child: Text('Something went wrong: ${snapshot.error}'),
            );
          }
          if (!snapshot.hasData || snapshot.data!.docs.isEmpty) {
            return const Center(child: Text('Your herd is empty.'));
          }

          final allDocs = snapshot.data!.docs;

          // ЁЯФ╜ Apply filter (All / Cattle / Buffalo)
          final filteredDocs = allDocs.where((doc) {
            final data = doc.data() as Map<String, dynamic>;
            final breed = (data['breed'] ?? '').toString();
            final isBuffalo = _isBuffaloBreed(breed);

            switch (_selectedFilter) {
              case HerdFilter.all:
                return true;
              case HerdFilter.cattle:
                return !isBuffalo;
              case HerdFilter.buffalo:
                return isBuffalo;
            }
          }).toList();

          if (filteredDocs.isEmpty) {
            return Column(
              children: [
                _buildFilterBar(onSurfaceColor),
                const Expanded(
                  child: Center(
                    child: Text('No animals found for this filter.'),
                  ),
                ),
              ],
            );
          }

          return Column(
            children: [
              _buildFilterBar(onSurfaceColor),
              Expanded(
                child: ListView.builder(
                  padding: const EdgeInsets.all(16.0),
                  itemCount: filteredDocs.length,
                  itemBuilder: (context, index) {
                    final animalDoc = filteredDocs[index];

                    return Dismissible(
                      key: Key(animalDoc.id),
                      direction: DismissDirection.endToStart,
                      onDismissed: (direction) {
                        _deleteAnimal(animalDoc);
                      },
                      background: Container(
                        color: Colors.red,
                        padding:
                        const EdgeInsets.symmetric(horizontal: 20),
                        alignment: Alignment.centerRight,
                        child:
                        const Icon(Icons.delete, color: Colors.white),
                      ),
                      child: _buildAnimalCard(
                        context,
                        animalDoc,
                        primaryColor,
                        onSurfaceColor,
                        _borderRadius,
                      ),
                    );
                  },
                ),
              ),
            ],
          );
        },
      ),
    );
  }

  /// ЁЯФ╜ Top filter UI
  Widget _buildFilterBar(Color onSurfaceColor) {
    return Padding(
      padding: const EdgeInsets.fromLTRB(16, 12, 16, 4),
      child: Row(
        children: [
          Text(
            'Filter:',
            style: TextStyle(
              fontWeight: FontWeight.w600,
              color: onSurfaceColor.withOpacity(0.8),
            ),
          ),
          const SizedBox(width: 12),
          Expanded(
            child: DropdownButtonFormField<HerdFilter>(
              value: _selectedFilter,
              decoration: const InputDecoration(
                isDense: true,
                border: OutlineInputBorder(),
                contentPadding:
                EdgeInsets.symmetric(horizontal: 12, vertical: 8),
              ),
              items: const [
                DropdownMenuItem(
                  value: HerdFilter.all,
                  child: Text('All (Cattle + Buffalo)'),
                ),
                DropdownMenuItem(
                  value: HerdFilter.cattle,
                  child: Text('Cattle Only'),
                ),
                DropdownMenuItem(
                  value: HerdFilter.buffalo,
                  child: Text('Buffalo Only'),
                ),
              ],
              onChanged: (value) {
                if (value == null) return;
                setState(() {
                  _selectedFilter = value;
                });
              },
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildAnimalCard(
      BuildContext context,
      DocumentSnapshot animalDoc,
      Color primaryColor,
      Color onSurfaceColor,
      double borderRadius,
      ) {
    final animalData = animalDoc.data() as Map<String, dynamic>;
    final imageBase64 = animalData['imageBase64'] as String? ?? '';
    final imageUrl = animalData['imageUrl'] as String? ?? ''; // fallback old data
    final name = animalData['name'] ?? 'Unnamed';
    final breed = animalData['breed'] ?? 'Unknown Breed';
    final confidence =
        (animalData['confidence'] as num?)?.toDouble() ?? 0.0;
    final timestamp = animalData['timestamp'] as Timestamp?;

    ImageProvider? imageProvider;
    if (imageBase64.isNotEmpty) {
      try {
        imageProvider = MemoryImage(base64Decode(imageBase64));
      } catch (e) {
        print('Error decoding base64 image: $e');
      }
    } else if (imageUrl.isNotEmpty) {
      imageProvider = NetworkImage(imageUrl);
    }

    return Card(
      elevation: 3,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(borderRadius),
      ),
      margin: const EdgeInsets.only(bottom: 15.0),
      child: InkWell(
        onTap: () {
          Navigator.of(context).push(
            MaterialPageRoute(
              builder: (_) => AnimalDetailScreen(animalDoc: animalDoc),
            ),
          );
        },
        onLongPress: () {
          _showEditNameDialog(animalDoc); // long press to rename
        },
        borderRadius: BorderRadius.circular(borderRadius),
        child: Padding(
          padding:
          const EdgeInsets.symmetric(vertical: 12.0, horizontal: 16.0),
          child: Row(
            children: [
              CircleAvatar(
                radius: 30,
                backgroundImage: imageProvider,
                child: imageProvider == null
                    ? const Icon(Icons.pets, size: 28)
                    : null,
              ),
              const SizedBox(width: 16),
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      name,
                      style: const TextStyle(
                        fontWeight: FontWeight.bold,
                        fontSize: 18,
                      ),
                    ),
                    const SizedBox(height: 4),
                    Text(
                      breed,
                      style: TextStyle(
                        fontSize: 14,
                        color: onSurfaceColor.withOpacity(0.8),
                      ),
                    ),
                    Text(
                      'Confidence: ${(confidence * 100).toStringAsFixed(1)}%',
                      style: TextStyle(
                        fontSize: 14,
                        color: onSurfaceColor.withOpacity(0.7),
                      ),
                    ),
                    if (timestamp != null)
                      Text(
                        'Added: ${DateFormat('MMM d, yyyy').format(timestamp.toDate())}',
                        style: TextStyle(
                          fontSize: 12,
                          color: onSurfaceColor.withOpacity(0.5),
                        ),
                      ),
                  ],
                ),
              ),
              Icon(Icons.chevron_right,
                  color: onSurfaceColor.withOpacity(0.5), size: 22),
            ],
          ),
        ),
      ),
    );
  }
}

